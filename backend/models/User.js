const mongoose = require("mongoose");
const { Schema } = mongoose; // Use destructuring to get Schema from mongoose

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const User = mongoose.model("user", UserSchema); // Create a model from Schema
module.exports = User;